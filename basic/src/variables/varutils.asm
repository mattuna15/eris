;
;						Erase variable hash tables
;
; *****************************************************************************

.VarEraseHashTables
		mov 	r0,#variableHashTable
		mov 	r1,#hashTableSize*4
._VEHTLoop
		stm 	r14,r0,#0
		inc 	r0	
		dec 	r1
		skz 	r1
		jmp 	#_VEHTLoop
		ret
