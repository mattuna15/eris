.Command_Load	;; [load]
		push 	link
		jsr 	#EvaluateString
		mov 	r1,r0,#0
		mov 	r0,#1
		jsr 	#OSFileOperation
		skz 	r0
		jmp 	#LoadError
		jsr 	#Command_Clear
		pop 	link
		ret
